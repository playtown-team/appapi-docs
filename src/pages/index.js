import React from 'react';
import clsx from 'clsx';
import Layout from '@theme/Layout';
import Link from '@docusaurus/Link';
import useDocusaurusContext from '@docusaurus/useDocusaurusContext';
import useBaseUrl from '@docusaurus/useBaseUrl';
import styles from './styles.module.css';

const features = [
  {
    title: <>Hecho en wordpress</>,
    imageUrl: 'img/wordpress-circular-logo.svg',
    description: (
      <>
        Pensado para que se ajuste a la estructura actual.
      </>
    ),
  },
  {
    title: <>Traductor para código nativo</>,
    imageUrl: 'img/react-native-circular-logo.svg',
    description: (
      <>
        Traduce los elementos más comúnes de wordpress en componentes que React Native pueda leer
      </>
    ),
  },
  {
    title: <>100% Modular con React Native</>,
    imageUrl: 'img/modular-circular-logo.svg',
    description: (
      <>
        Permite solamente tener que desarrollar nuevos traductores para módulos que convivan en wordpress y cualquier app.
      </>
    ),
  },
];

function Feature({ imageUrl, title, description }) {
  const imgUrl = useBaseUrl(imageUrl);
  return (
    <div className={clsx('col col--4', styles.feature)}>
      {imgUrl && (
        <div className="text--center">
          <img className={styles.featureImage} src={imgUrl} alt={title} />
        </div>
      )}
      <h3 style={{textAlign:"center"}}>{title}</h3>
      <p style={{textAlign:"center"}}>{description}</p>
    </div>
  );
}

function Home() {
  const context = useDocusaurusContext();
  const { siteConfig = {} } = context;
  return (
    <Layout
      title={`Hello from ${siteConfig.title}`}
      description="Description will go into a meta tag in <head />">
      <header className={clsx('hero hero--primary', styles.heroBanner)}>
        <div className="container">
          <h1 className="hero__title">{siteConfig.title}</h1>
          <p className="hero__subtitle">{siteConfig.tagline}</p>
          <div className={styles.buttons}>
            <Link
              className={clsx(
                'button button--outline button--secondary button--lg',
                styles.getStarted,
              )}
              to={useBaseUrl('docs/')}>
              Empezar
            </Link>
          </div>
        </div>
      </header>
      <main>
        {features && features.length > 0 && (
          <section className={styles.features}>
            <div className="container">
              <div className="row">
                {features.map((props, idx) => (
                  <Feature key={idx} {...props} />
                ))}
              </div>
            </div>
          </section>
        )}
      </main>
    </Layout>
  );
}

export default Home;
